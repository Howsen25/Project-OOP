    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // BMI:
        double h = Double.parseDouble(jTextField2.getText());
        h = h/100;
        double w = Double.parseDouble(jTextField3.getText());

        double bmi = w / (h*h);

        String calbmi = String.format("%.2f",bmi);

        jTextField4.setText(calbmi);

        if(bmi<=18.5){
            jTextField4.setOpaque(true);
            jTextField4.setBackground(Color.blue);
            jLabel8.setForeground(Color.blue);
            jLabel8.setText("Under Weight");
        }else if(bmi<=24.9){
            jTextField4.setOpaque(true);
            jTextField4.setBackground(Color.green);
            jLabel8.setForeground(Color.green);
            jLabel8.setText("Normal");
        }else if(bmi<=29.9){
            jTextField4.setOpaque(true);
            jTextField4.setBackground(Color.orange);
            jLabel8.setForeground(Color.orange);
            jLabel8.setText("Over Weight");
        }else{
            jTextField4.setOpaque(true);
            jTextField4.setBackground(Color.red);
            jLabel8.setForeground(Color.red);
            jLabel8.setText("Obese");
        }

        // BMR:
        class BMR {
           protected double weight;

           public BMR(double weight) {
             this.weight = weight;
           }

           public double calculate() {
             return 0;
           }
         }

         class MaleBMR extends BMR {
           public MaleBMR(double weight) {
             super(weight);
           }

           @Override
           public double calculate() {
             return (15.3*weight) + 679;
           }
         }

         class FemaleBMR extends BMR {
           public FemaleBMR(double weight) {
             super(weight);
           }

           @Override
           public double calculate() {
             return (14.7*weight) + 496;
           }
         }

         if(jToggleButton1.isSelected()){
                     FemaleBMR female = new FemaleBMR(w);
                     double bmr = female.calculate();
                        String calbmr = String.format("%.2f",bmr);
                        jTextField5.setText(calbmr);

                 }else if(jToggleButton2.isSelected()){
                     MaleBMR male = new MaleBMR(w);
                     double bmr = male.calculate();
                        String calbmr = String.format("%.2f",bmr);
                        jTextField5.setText(calbmr);
                 }

        // water:

        double water = (0.5)*(w*2.2) /33.81;
        String calwater = String.format("%.2f",water);
        jTextField6.setText(calwater);
    }//GEN-LAST:event_jButton3ActionPerformed

